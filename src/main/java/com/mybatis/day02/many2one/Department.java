package com.mybatis.day02.many2one;

public class Department {
    private String name;
    private Integer id;


    public String getName() {
        return name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
